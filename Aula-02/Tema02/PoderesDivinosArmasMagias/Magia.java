package PoderesDivinosArmasMagias;

public class Magia {
    public String nome;
    public int poder;
    public int custoDeMana;
    public String tipo;


    public Magia(String nome, int poder, int custoDeMana, String tipo) {
        this.nome = nome;
        this.poder = poder;
        this.custoDeMana = custoDeMana;
        this.tipo = tipo;
    }
}
